# ==========================================
# DAILY API BUDGET GUARD
# Tracks eBay API calls in Redis with a 24hr TTL.
# Stops all scans once DAILY_API_BUDGET is reached.
# Resets automatically at midnight UTC when key expires.
# ==========================================
# Per-task soft budget allocations — prevents any single task from
# consuming the entire daily budget and starving other tasks.
# These are SOFT limits logged as warnings, not hard stops.
# The DAILY_API_BUDGET is the only hard stop.
BUDGET_ALLOCATIONS = {
    "prewarm":     1000,   # Once/day cold run
    "van_prewarm":  200,   # Once/day cold run
    "sniper":      1500,   # 24 runs/day × 5 queries × ~3 calls avg = ~360/day
    "van_sniper":   700,   # 24 runs/day × ~29 calls avg
    "sweep":       1000,   # 6 runs/day × ~136 calls avg
    "van_sweep":    700,   # 4 runs/day × ~136 calls avg
}
TASK_BUDGET_KEY_PREFIX = "ebay_task_calls"

def _check_budget(calls_needed: int = 1, task_name: str = None) -> bool:
    """Returns True if budget allows, increments counter. False = stop scanning."""
    pipe = redis_client.pipeline()
    pipe.incrby(DAILY_BUDGET_KEY, calls_needed)
    pipe.ttl(DAILY_BUDGET_KEY)
    result = pipe.execute()
    current_count = result[0]
    ttl = result[1]

    # Set 24hr expiry on first write
    if ttl < 0:
        redis_client.expire(DAILY_BUDGET_KEY, 86400)

    remaining = DAILY_API_BUDGET - current_count
    if current_count % 500 == 0:
        print(f"📊 Budget: {current_count}/{DAILY_API_BUDGET} used ({remaining} remaining)")

    if current_count > DAILY_API_BUDGET:
        print(f"🛑 Daily budget exceeded: {current_count}/{DAILY_API_BUDGET} calls used")
        return False

    # Soft per-task warning
    if task_name and task_name in BUDGET_ALLOCATIONS:
        task_key = f"{TASK_BUDGET_KEY_PREFIX}:{task_name}"
        task_count = int(redis_client.incr(task_key))
        if ttl < 0:
            redis_client.expire(task_key, 86400)
        soft_limit = BUDGET_ALLOCATIONS[task_name]
        if task_count > soft_limit:
            print(f"⚠️ [{task_name}] soft budget exceeded: {task_count}/{soft_limit} — continuing but investigate")

    return True


# ==========================================
# SHARED SCAN ENGINE
# ==========================================
def run_scan(dealer_id: int, mode_name: str, listings_to_pull: int, keywords=None, sort="newlyListed", source_override=None, query_groups_override=None, since=None):
    """
    Unified scan engine.

    Sniper:      single make group, sort=newlyListed, offset=0 — freshest listings.
    Value sweep: all make groups, dual strategy per group:
                   A) sort=price, offset=100 + 40 — stale cheap stock
                   B) sort=bestMatch, offset=0    — recently price-dropped/relisted
    """

    lock_key = f"scan_lock_{dealer_id}_{mode_name}"
    max_expansions = VALUE_SWEEP_LIMIT if mode_name == "value_sweep" else SNIPER_LIMIT

    if redis_client.get(lock_key):
        print("⚠️ Scan already running — skipping")
        return {"skipped": True}

    redis_client.set(lock_key, "1", ex=1800)

    db = SessionLocal()

    try:
        dealer = db.query(Dealer).filter(Dealer.id == dealer_id).first()
        if not dealer:
            return {"error": "Dealer not found"}

        settings = db.query(DealerSettings).filter(
            DealerSettings.dealer_id == dealer.id
        ).first()

        if not settings:
            return {"error": "Dealer settings missing"}

        filters = {
            "min_year": settings.min_year,
            "max_year": settings.max_year,
            "max_mileage": settings.max_mileage,
            "max_price": settings.max_price if settings.max_price else 50000,
            "min_profit": settings.min_profit,
            "min_score": settings.min_score,
        }
        buyer_postcode   = getattr(settings, "search_postcode", None)
        radius_miles     = getattr(settings, "search_radius_miles", None)
        if buyer_postcode and radius_miles:
            print(f"📍 Location filter: {radius_miles} miles from {buyer_postcode}")

        base_groups = query_groups_override if query_groups_override is not None else SCAN_QUERY_GROUPS
        query_groups = [keywords] if keywords is not None else base_groups

        total_listings = 0
        total_deals = 0
        processed_ids = set()
        detail_expansions = 0

        for source_name in SOURCES:

            source = get_listing_source(source_name)
            items = []

            for query in query_groups:
                print(f"🔍 [{mode_name}] Searching: '{query}'")

                if mode_name == "value_sweep":
                    budget_ok = True
                    # -------------------------------------------------------
                    # Strategy A: price-ascending, offset 80 then 120
                    # Lands in stale/overlooked cheap stock beyond page 1.
                    # Offsets must be multiples of limit (40) per eBay API.
                    # -------------------------------------------------------
                    for offset in [40, 80, 120, 160, 200]:

                        task_name = "van_sweep" if source_override == "ebay_vans" else "sweep"
                        if not _check_budget(1, task_name):
                            print("🛑 Daily API budget reached — stopping sweep (strategy A)")
                            budget_ok = False
                            break

                        page_items = source.search(
                            keywords=query,
                            entries=listings_to_pull,
                            min_price=None,
                            max_price=filters["max_price"],
                            sort="price",
                            offset=offset,
                            buyer_postcode=buyer_postcode,
                            radius_miles=radius_miles,
                        )

                        items.extend(page_items)

                    # -------------------------------------------------------
                    # Strategy B: bestMatch, offset 0
                    # eBay boosts recently price-dropped or relisted cars.
                    # Catches motivated sellers that newlyListed won't show.
                    # -------------------------------------------------------
                    if budget_ok:
                        task_name = "van_sweep" if source_override == "ebay_vans" else "sweep"
                        if not _check_budget(1, task_name):
                            print("🛑 Daily API budget reached — stopping sweep (strategy B)")
                        else:
                            page_items = source.search(
                                keywords=query,
                                entries=listings_to_pull,
                                min_price=None,
                                max_price=filters["max_price"],
                                sort="bestMatch",
                                offset=0,
                                buyer_postcode=buyer_postcode,
                                radius_miles=radius_miles,
                            )
                            items.extend(page_items)


                else:
                    # Sniper: multi-window search strategy
                    task_name = "van_sniper" if source_override == "ebay_vans" else "sniper"

                    if source_name == "ebay_browse":
                        # search_sniper_windows fires 5 price-window calls — count all 5
                        # so the budget tracker stays accurate. Counting 1 was causing
                        # the daily budget to appear 5× healthier than reality.
                        if not _check_budget(5, task_name):
                            print("Daily API budget reached - stopping sniper")
                            break

                        sniper_items = search_sniper_windows(
                            query, "",
                            since=since,
                            buyer_postcode=buyer_postcode,
                            radius_miles=radius_miles,
                        )
                        items.extend(sniper_items)

                    else:
                        page_items = source.search(
                            keywords=query,
                            entries=listings_to_pull,
                            min_price=None,
                            max_price=filters["max_price"],
                            sort=sort,
                            offset=0,
                        )
                        items.extend(page_items)

            total_listings += len(items)

            for item in items:

                if detail_expansions >= max_expansions:
                    print("🛑 Expansion cap reached")
                    break

                external_id = item.get("id") or item.get("view_url")
                if not external_id or external_id in processed_ids:
                    continue

                processed_ids.add(external_id)

                # -------------------------------------------------------
                # SNIPER: listing age gate
                # Only evaluate listings created in the last 90 minutes.
                # This ensures the sniper is doing what it says — catching
                # cars within an hour of listing. Without this, it re-scores
                # the same inventory on every rotation, burning budget.
                # -------------------------------------------------------
                if mode_name == "sniper":
                    listing_date_raw = item.get("listing_date")
                    if listing_date_raw:
                        try:
                            listed_at = datetime.fromisoformat(listing_date_raw.replace("Z", "+00:00"))
                            age_minutes = (datetime.now(timezone.utc) - listed_at).total_seconds() / 60
                            if age_minutes > SNIPER_AGE_GATE_MINUTES:
                                continue  # Too old for sniper — skip silently
                        except Exception:
                            pass  # Can't parse date — let it through

                # -------------------------------------------------------
                # SNIPER: cross-run deduplication via Redis
                # Prevents re-evaluating the same listing across consecutive
                # 10-minute sniper rotations. 6hr TTL covers a full day cycle.
                # Without this, a £3k Ford is re-evaluated every 30 minutes.
                # -------------------------------------------------------
                if mode_name == "sniper":
                    seen_key = f"{SNIPER_SEEN_KEY}:{external_id}"
                    if redis_client.exists(seen_key):
                        continue  # Already evaluated this run cycle
                    redis_client.set(seen_key, "1", ex=SNIPER_SEEN_TTL)

                price_val = item.get("price")

                if not price_val:
                    continue

                rough_price = float(price_val)

                item_id = item.get("id")

                # -------------------------------------------------------
                # PRICE DROP TRACKING
                # Store first-seen price with 7-day TTL. On each subsequent
                # sweep, compare against first-seen to detect price reductions.
                # A drop >£200 or >5% is a real signal — motivated seller.
                # -------------------------------------------------------
                price_drop_amount = None
                price_drop_pct = None

                if item_id:
                    first_price_key = f"{FIRST_PRICE_KEY}:{item_id}"
                    first_price_raw = redis_client.get(first_price_key)

                    if first_price_raw:
                        first_price_val = float(first_price_raw)
                        if rough_price < first_price_val:
                            drop_amount = first_price_val - rough_price
                            drop_pct_val = drop_amount / first_price_val
                            if drop_amount >= 200 or drop_pct_val >= 0.05:
                                price_drop_amount = round(drop_amount, 2)
                                price_drop_pct = round(drop_pct_val * 100, 1)
                                print(f"📉 Price drop: £{first_price_val} → £{rough_price} (−£{price_drop_amount} / −{price_drop_pct}%)")
                    else:
                        # First time we see this listing — record original price
                        redis_client.set(first_price_key, rough_price, ex=FIRST_PRICE_TTL)

                    # Always update last-seen price for sweep comparisons
                    last_price_key = f"{PRICE_TRACK_KEY}:{item_id}"
                    redis_client.set(last_price_key, rough_price, ex=86400)

                # Attach price drop signals to item for deal_engine to surface
                item["price_drop_amount"] = price_drop_amount
                item["price_drop_pct"] = price_drop_pct

                if not rough_price:
                    continue

                if rough_price > filters["max_price"]:
                    print(f"⛔ Pre-screen: £{rough_price} exceeds max £{filters['max_price']} — skipping")
                    continue

                # ------------------------------------------
                # PRICE EXPECTATION GATE
                # Uses prewarm cache to skip listings priced
                # too close to market value before running
                # expensive full valuation.
                # ------------------------------------------
                try:
                    title = item.get("title", "")

                    year_match = YEAR_PATTERN.search(title)
                    year = int(year_match.group(1)) if year_match else None

                    mileage_match = MILEAGE_PATTERN.search(title.lower())
                    mileage = int(mileage_match.group(1)) * 1000 if mileage_match else None

                    title_words = title.split()

                    make = None
                    model = None

                    # All makes in SCAN_QUERY_GROUPS — keep in sync
                    _GATE_MAKES = {
                        "ford","vauxhall","volkswagen","vw","audi","bmw","mercedes",
                        "toyota","nissan","honda","hyundai","kia","land","peugeot",
                        "renault","skoda","seat","mazda","volvo","mini","citroen",
                        "jaguar","fiat","alfa","dacia","mg","mitsubishi","subaru",
                        "suzuki","jeep","tesla","lexus","porsche","isuzu","saab",
                        "ssangyong","smart","chrysler","ds","infiniti",
                    }

                    for i, word in enumerate(title_words):
                        if word.lower() in _GATE_MAKES:
                            make = word
                            if i + 1 < len(title_words):
                                model = title_words[i + 1]
                            break

                    if make and model and year:

                        valuation = get_market_price_from_sold(
                            make,
                            model,
                            year,
                            mileage,
                            cache_only=True
                        )

                        if valuation and valuation.get("market_price"):
                            gate_confidence = valuation.get("confidence", "low")

                            # Only use the gate when confidence is high — medium/low
                            # means thin sold data (few items, no mileage weighting).
                            # A weak cached value must not block a listing from full
                            # evaluation; it could easily be wrong by 30%+.
                            if gate_confidence != "high":
                                pass  # let it through for full evaluation
                            else:
                                expected_price = valuation["market_price"]
                                if rough_price > expected_price * 0.85:
                                    print(
                                        f"🚫 Gate skip: £{rough_price} too close to market "
                                        f"(£{expected_price}) [{gate_confidence} confidence]"
                                    )
                                    continue

                except Exception as e:
                    print(f"Gate check failed: {e}")

                if source_override == "ebay_vans":
                    task_name = "van_sniper" if mode_name == "sniper" else "van_sweep"
                else:
                    task_name = "sniper" if mode_name == "sniper" else "sweep"

                if not _check_budget(1, task_name):
                    print("🛑 Daily API budget reached — stopping expansions")
                    break

                deal = process_listing(
                    item,
                    dealer.id,
                    source=source_override or source_name,
                    filters=filters,
                    budget_fn=lambda n: _check_budget(
                        n,
                        "van_sniper" if source_override == "ebay_vans" and mode_name == "sniper"
                        else "van_sweep" if source_override == "ebay_vans"
                        else "sniper" if mode_name == "sniper"
                        else "sweep"
                    )
                )

                # Price drop updates re-evaluate an existing deal — no eBay API
                # calls were made, so don't charge the expansion budget.
                _is_price_drop = (
                    deal is not None
                    and isinstance(getattr(deal, "report", None), dict)
                    and deal.report.get("deal_signals", {}).get("is_price_drop_alert")
                )
                if not _is_price_drop:
                    detail_expansions += 1
                gc.collect()  # Free image/OCR memory between expansions

                if not deal:
                    continue

                total_deals += 1
                notify_deal.delay(deal.id)

        scan = ScanRun(
            dealer_id=dealer.id,
            source=f"mode_{mode_name}",
            listings_found=total_listings,
            deals_saved=total_deals
        )

        db.add(scan)
        db.commit()

        return {
            "mode": mode_name,
            "listings_found": total_listings,
            "deals_saved": total_deals
        }

    finally:
        redis_client.delete(lock_key)
        db.close()


